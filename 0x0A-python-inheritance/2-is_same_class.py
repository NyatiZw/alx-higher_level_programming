#!/usr/bin/python3
"""
Function that returns True if object is instance of class


"""

def is_same_class(obj, a_class):
    """
    Function that returns true if object is instance of class

    Args:
        obj: object
        a_class: class

    Returns:
        True: if object is instance of class
        False: if object is not an instance of class

    """


    if obj == isinstance(obj, a_class):
        return True
    else:
        return False
